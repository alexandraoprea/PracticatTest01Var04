package ro.pub.cs.systems.eim.practicaltest01var04;

import android.content.Intent;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.TextView;

import androidx.appcompat.app.AppCompatActivity;


public class PracticalTest01Var04SecondaryActivity extends AppCompatActivity {

    private TextView userNameView, userGroupView;
    private Button okButton, cancelButton;

    private ButtonClickListener buttonClickListener = new ButtonClickListener();
    private class ButtonClickListener implements View.OnClickListener {
        @Override
        public void onClick(View view) {
            switch (view.getId()) {
                case R.id.ok_button:
                    setResult(RESULT_OK, null);
                    break;
                case R.id.cancel_button:
                    setResult(RESULT_CANCELED, null);
                    break;
            }
            finish();
        }
    }

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_practical_test01_var04_secondary);

        userNameView = (TextView)findViewById(R.id.user_name);
        userGroupView = (TextView)findViewById(R.id.group_name);
        Intent intent = getIntent();
        String userName = intent.getStringExtra(Constants.USER_NAME);
        String groupName = intent.getStringExtra(Constants.GROUP_NAME);

        userNameView.setText(String.valueOf(userName));
        userGroupView.setText(String.valueOf(groupName));

        okButton = (Button)findViewById(R.id.ok_button);
        okButton.setOnClickListener(buttonClickListener);
        cancelButton = (Button)findViewById(R.id.cancel_button);
        cancelButton.setOnClickListener(buttonClickListener);
    }
}
